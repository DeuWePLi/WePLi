    private void chartScrollPanelMouseWheelMoved(java.awt.event.MouseWheelEvent evt) {//GEN-FIRST:event_chartScrollPanelMouseWheelMoved
        // TODO add your handling code here:
        int TableRowHeight = searchTable.getRowHeight();
        if (evt.getUnitsToScroll() > 0) {
            chartScrollPanel.getVerticalScrollBar().setValue(chartScrollPanel.getVerticalScrollBar().getValue() + TableRowHeight); // 각 행의 Height 만큼씩 Scroll이 움직인다.
            //TableRowHeight 자리에 원하는 숫자를 넣어 스크롤 속도를 조절할 수 있다.
        } else {
            chartScrollPanel.getVerticalScrollBar().setValue(chartScrollPanel.getVerticalScrollBar().getValue() - TableRowHeight);
        }
    }//GEN-LAST:event_chartScrollPanelMouseWheelMoved

    private void SearchButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_SearchButtonMouseClicked
        // TODO add your handling code here:
        // 검색 버튼 클릭 시
        
        SongController songController = new SongController(); // 컨트롤러 생성
        
        String musicSite = siteRadioGroup.getSelection().getActionCommand();
        String searchText = SearchTextField.getText();
        
        ArrayList<SongDto> searchResult = songController.SongSearch(musicSite,searchText); //검색 결과 리턴
        System.out.println(searchResult);
        Object[][] values = songDtoToObject(searchResult);
        

        DefaultTableModel model = (DefaultTableModel)searchTable.getModel();
        model.setRowCount(0);
                
        JTableSetting.insertTableRow((DefaultTableModel) searchTable.getModel(), values);
        
        
    }//GEN-LAST:event_SearchButtonMouseClicked
    public Object[][] songDtoToObject(ArrayList<SongDto> songArray){
        
       
        Object[][] o = new Object[songArray.size()][];
        for (int i = 0; i < songArray.size(); i++) {
            SongDto song = songArray.get(i);
            o[i] = new Object[] {i+1,song.getCoverImg(),song.getTitle(),song.getSinger(),song.getAlbum()};
        }
        
        
        return o;
    }
    
    
    
    public ImageIcon imageToIcon(String url) {
        try {
            /* 웹 이미지 가져오기 */
            ImageIcon icon = new ImageIcon(new URL(url));
            
            /* 이미지 사이즈 조정 */
            return new ImageIcon(icon.getImage().getScaledInstance(60, 60, Image.SCALE_SMOOTH));
        }
        catch(MalformedURLException e){
            return null;
        }
    }   
    
    
   
